    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $this->validate($request, [
            'file_name' => 'required|mimes:pdf,jpg,jpeg,png,gif',

        ], [
            'file_name.mimes' => 'the extension of the file should be pdf,jpg,jpeg,png,gif',
        ]);
        if ($request->hasFile('file_name')) {


            $image = $request->file('file_name');
            $file_name = $image->getClientOriginalName();
            $invoice_number = $request->invoice_number;

            $attachments = new invoice_attachments();
            $attachments->file_name = $file_name;
            $attachments->invoice_number = $invoice_number;
            $attachments->created_by = Auth::user()->name;
            $attachments->invoice_id = $request->invoice_id;
            $attachments->save();

            //move pic
            $image_name = $request->file_name->getClientOriginalName();
            $request->file_name->move( public_path('attachments/' . $invoice_number) ,$image_name);
        }
        session()->flash('Add', 'تم اضافة الفاتورة بنجاح');
        return back();
    }

